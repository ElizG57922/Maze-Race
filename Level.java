	public static int[][] generateRandomLevel(int[][]board){
		Random rn = new Random();
		for (int i = 0; i < board.length; i++) {
			for (int j = 0; j < board[i].length; j++) {
				int determiner = rn.nextInt(5);
				if(determiner < 4)
    				board[i][j] = 0;
				else
					board[i][j] = 1;
			}
		}
		board[board.length - 1][board[0].length - 1] = 2;
		return board;
	}
